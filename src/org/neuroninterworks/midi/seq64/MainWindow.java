    private void jMenuMidiClockInputMouseEntered(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jMenuMidiClockInputMouseEntered

        jMenuMidiClockInput.removeAll();
        for (Object device : MidiCommon.getInputDevices()) {
            MidiDevice.Info info = MidiCommon.getMidiDeviceInfo((String) device, false);  // WINDOOM - MICRO MODULAR
            try {
                final MidiDevice md = MidiSystem.getMidiDevice(info);

                final JRadioButtonMenuItem i = new JRadioButtonMenuItem(new AbstractAction(info.getName()) {
                    public void actionPerformed(ActionEvent e) {
                        launchpad.getMainSequencer().setClockDevice(md);
                    }
                });
                if (launchpad.getMainSequencer().getClockDevice() == md) {
                    i.setSelected(true);
                }

                jMenuMidiClockInput.add(i);
            } catch (MidiUnavailableException ex) {
                Logger.getLogger(MainWindow.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jMenuMidiClockInputMouseEntered

