    private class MyDispatcher implements KeyEventDispatcher {

        
        @Override
        public boolean dispatchKeyEvent(KeyEvent e) {
            if (launchpad.dispatchKeys == false) return false;
            if (e.getID() == KeyEvent.KEY_PRESSED) {
                char i = e.getKeyChar();
                String str = Character.toString(i);
//              System.out.println("KeyEvent.KEY_PRESSED: " + str);

                GroovyPlug plugin = launchpad.groovyPlugs.get(str);
                OutputTextArea.append("dispatchKeyEvent " + str + "\n");
                if (plugin != null) {
                    plugin.exec(OutputTextArea);
                }

            } else if (e.getID() == KeyEvent.KEY_RELEASED) {
//                System.out.println("2test2");
            } else if (e.getID() == KeyEvent.KEY_TYPED) {
//                System.out.println("3test3");
            }
            return false;
        }
    }

